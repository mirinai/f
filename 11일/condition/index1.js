//조건 - if/else-if/else
//조건 -switch

const season = "spring";

switch (season) {
  case "spring":
    console.log("봄");
    break;
  case "summer":
    console.log("여름");
    break;
  case "fall":
    console.log("가을");
    break;
  case "winter":
    console.log("겨울");
    break;
  default:
    console.log("몰?루");
    break;
}
